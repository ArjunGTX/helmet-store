import { createSlice } from "@reduxjs/toolkit";

const initialState = {
  isOpen: false,
};

const sidebarSlice = createSlice({
  name: "sidebar",
  initialState,
  reducers: {
    open: (state) => {
      state.isOpen = true;
    },
    close: (state) => {
      state.isOpen = false;
    },
  },
});

export const { open, close } = sidebarSlice.actions;

export const selectIsSidebarOpen = (store) => store.sidebar.isOpen;

export const sidebarReducer = sidebarSlice.reducer;
